metro = float(input("Digite uma distância em metros: "))
centimetros = metro*100
milimetros = centimetros*10

print("O valor em metros digitado foi: {}".format(metro))
print("O valor digitado convertido em centímetros é: {}".format(centimetros))
print("O valor digitado convertido em milímetros é: {}".format(milimetros))
